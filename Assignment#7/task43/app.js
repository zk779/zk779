// program to check leap year
var str = prompt('Enter a Character or Number :');
//three conditions to find out the leap year
if ((str >= 'a') && (str <= 'z') || (str >= 'A' && str <= 'Z')) {
    console.log(str + ' is an Alphabet');
}
else {
    console.log(str + ' is not an Alphabet');
}
